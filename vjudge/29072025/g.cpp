int n;
ll computate(ll m, vector<int> &p){
	ordered_multiset s;
	
	ll sum = 0;
	for(int i = 0; i<n; i++){
		if(i) s.insert(m+p[i-1]);
		else s.insert(m);
		sum += s.size() - s.order_of_key(p[i]);
		debug(s);
	}

	return sum;
}

int main(){
	int k; cin>>n>>k;
	vector<int> v(n), p(n);

	int i = 0;
	for(int &e: v){
		cin>>e;
		p[i] = e;
		if(i) p[i] += p[i-1];
		i++;
	}

	// Quantas somas menores que X existem no array:
	// significa -> Quantos i, j tais que i <= j e p[j]-p[i-1] <= X
	// ou p[j] <= X + p[i-1] 
	//


	ll l = 0, r = 1e16;
	ll m = l + (r-l)/2;


	while(l < r){
		ll m = l + (r-l)/2;
		if(computate(m, p)>k) r = m;
		else l = m+1;
		// usleep(2 * microsecond);//sleeps for 3 second
	}

	cout<<l<<endl;

}